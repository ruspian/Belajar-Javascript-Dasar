// template literal
// template literal adalah cara untuk membuat string
// template literal digunakan untuk membuat string dengan menggunakan backtick ``

const nama = "ucup";
const umur = 20;

console.log(`halo nama saya ${nama}, saya ${umur} tahun`);

// menjalankan ternari operator dalam template literal
const x = 10;
console.log(`x adalah bilangan: ${x % 2 == 0 ? "genap" : "ganjil"}`);
