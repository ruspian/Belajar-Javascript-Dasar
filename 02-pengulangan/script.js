// pengulangan for
console.log("Pengulangan for");
for (let i = 0; i < 10; i++) {
  console.log("Ini adalah perulangan ke-" + (i + 1));
}

// pengulangan while
console.log("Pengulangan while");
let i = 1;
while (i <= 10) {
  console.log("Ini adalah perulangan ke-" + (i + 1));
  i++;
}

